const ASP_EXPLORER_CONFIG_FILE: &str = "agents/asp-explorer.toml";
const ASP_EXPLORER_DESCRIPTION: &str = "ASP search/query evidence explorer.";

#[derive(Debug)]
/// Status of the Codex user trust entries required for project hooks.
pub struct CodexUserTrustStatus {
    /// User-level Codex config containing hook trust hashes.
    pub trust_config_path: PathBuf,
    /// Whether Codex can load this project's local config and all hook events are trusted.
    pub trusted: bool,
    /// Whether the user-level Codex config trusts this project root.
    pub project_trusted: bool,
    /// Whether all required hook event hashes match the current project config.
    pub hook_state_trusted: bool,
    /// Hook event names still missing trusted state.
    pub missing_events: Vec<String>,
    /// Hook event names with trusted state that no longer matches the current hook block.
    pub stale_events: Vec<String>,
}

#[derive(Debug, Clone, Copy)]
struct CodexHookEvent {
    event: &'static str,
    state_label: &'static str,
    matcher: Option<&'static str>,
    status: &'static str,
    hook_event: &'static str,
}

const CLAUDE_MANAGED_COMMAND_MARKER: &str = "# Generated by asp install hook --client claude.";

struct ClaudeHookEvent {
    event: &'static str,
    matcher: Option<&'static str>,
    hook_event: &'static str,
}

/// Render the managed Codex hook block that dispatches through `asp hook`.
pub fn codex_hook_block(project_root: &Path) -> String {
    let events = codex_hook_events();
    let body = events
        .iter()
        .map(|event| codex_hook_event_block(event, project_root))
        .collect::<Vec<_>>()
        .join("\n\n");
    format!(
        "{ROOT_BLOCK_BEGIN}\n# Generated by `asp install plugin --codex`.\n# ASP Explorer role registration for Codex spawn_agent.\n\n{}\n\n# Root dispatcher for activated semantic hook providers.\n\n{body}\n{ROOT_BLOCK_END}",
        codex_asp_explorer_role_block()
    )
}

pub fn codex_asp_explorer_role_block() -> String {
    format!(
        "[agents.{ASP_EXPLORER_ROLE_NAME}]\ndescription = {}\nconfig_file = {}\nnickname_candidates = [\"ASP Explore\", \"ASP Reasoning\", \"ASP Search\"]",
        toml_basic_string(ASP_EXPLORER_DESCRIPTION),
        toml_basic_string(ASP_EXPLORER_CONFIG_FILE),
    )
}

/// Merge the Codex ASP Explorer role registration into project config text.
///
/// Plugin-based installs keep hook registration in the plugin, but Codex still
/// needs this role entry to expose the read-only ASP custom agent.
pub fn merge_codex_asp_explorer_role_config(existing: &str) -> Result<String, String> {
    validate_codex_config_toml(existing)?;
    let parsed = toml::from_str::<toml::Value>(existing)
        .map_err(|error| format!("invalid Codex config TOML: {error}"))?;
    let has_role = parsed
        .get("agents")
        .and_then(toml::Value::as_table)
        .and_then(|agents| agents.get(ASP_EXPLORER_ROLE_NAME))
        .is_some();
    let prefix = existing.trim_end();
    if has_role {
        return Ok(if prefix.is_empty() {
            String::new()
        } else {
            format!("{prefix}\n")
        });
    }
    let role_block = codex_asp_explorer_role_block();
    Ok(if prefix.is_empty() {
        format!("{role_block}\n")
    } else {
        format!("{prefix}\n\n{role_block}\n")
    })
}

/// Install user-level Codex trust state for the managed project hook block.
pub fn install_codex_user_trust_state(project_config_path: &Path) -> Result<PathBuf, String> {
    let project_config_path = fs::canonicalize(project_config_path).map_err(|error| {
        format!(
            "failed to resolve project Codex config {}: {error}",
            project_config_path.display()
        )
    })?;
    let project_root = project_root_for_codex_config_path(&project_config_path)?;
    let codex_home = codex_home_path()?;
    fs::create_dir_all(&codex_home)
        .map_err(|error| format!("failed to create {}: {error}", codex_home.display()))?;
    let user_config_path = codex_home.join("config.toml");
    let existing = fs::read_to_string(&user_config_path).unwrap_or_default();
    if user_config_path.is_file() {
        validate_codex_config_toml(&existing).map_err(|error| {
            format!(
                "refusing to write invalid Codex user config {}: {error}",
                user_config_path.display()
            )
        })?;
    }
    let trust_block = codex_trust_state_block(&project_config_path, &project_root);
    let merged = merge_codex_project_trust_config(&existing, &project_root)?;
    let merged = merge_codex_trust_config(&merged, &project_config_path, &trust_block);
    validate_codex_config_toml(&merged).map_err(|error| {
        format!(
            "refusing to write invalid Codex user trust config {}: {error}",
            user_config_path.display()
        )
    })?;
    fs::write(&user_config_path, merged.as_bytes())
        .map_err(|error| format!("failed to write {}: {error}", user_config_path.display()))?;
    Ok(user_config_path)
}

/// Inspect user-level Codex trust state for a project hook config.
pub fn codex_user_trust_state_status(
    project_config_path: &Path,
) -> Result<CodexUserTrustStatus, String> {
    let project_config_path = fs::canonicalize(project_config_path).map_err(|error| {
        format!(
            "failed to resolve project Codex config {}: {error}",
            project_config_path.display()
        )
    })?;
    let project_root = project_root_for_codex_config_path(&project_config_path)?;
    let trust_config_path = codex_home_path()?.join("config.toml");
    let content = fs::read_to_string(&trust_config_path).unwrap_or_default();
    let parsed =
        toml::from_str::<toml::Value>(&content)
            .unwrap_or(toml::Value::Table(
                toml::map::Map::<String, toml::Value>::new(),
            ));
    let state = parsed
        .get("hooks")
        .and_then(toml::Value::as_table)
        .and_then(|hooks| hooks.get("state"))
        .and_then(toml::Value::as_table);
    let mut missing_events = Vec::new();
    let mut stale_events = Vec::new();
    for event in codex_hook_events() {
        let expected_hash = codex_hook_trusted_hash(&event, &project_root);
        match codex_hook_trust_hash(state, &project_config_path, &event) {
            Some(actual_hash) if actual_hash == expected_hash => {}
            Some(_) => stale_events.push(event.hook_event.to_string()),
            None => missing_events.push(event.hook_event.to_string()),
        }
    }
    let project_trusted = codex_project_trusted(&parsed, &project_root);
    let hook_state_trusted = missing_events.is_empty() && stale_events.is_empty();
    Ok(CodexUserTrustStatus {
        trust_config_path,
        trusted: project_trusted && hook_state_trusted,
        project_trusted,
        hook_state_trusted,
        missing_events,
        stale_events,
    })
}

fn codex_hook_trust_hash(
    state: Option<&toml::map::Map<String, toml::Value>>,
    project_config_path: &Path,
    event: &CodexHookEvent,
) -> Option<String> {
    let key = format!(
        "{}:{}:0:0",
        project_config_path.display(),
        event.state_label
    );
    state
        .and_then(|state| state.get(&key))
        .and_then(toml::Value::as_table)
        .and_then(|entry| entry.get("trusted_hash"))
        .and_then(toml::Value::as_str)
        .map(str::to_string)
}

/// Validate Codex config text as TOML before writing it back to disk.
pub fn validate_codex_config_toml(content: &str) -> Result<(), String> {
    toml::from_str::<toml::Value>(content)
        .map(|_| ())
        .map_err(|error| error.to_string())
}

/// Merge the managed hook block into existing Codex project config text.
pub fn merge_codex_config(existing: &str, block: &str) -> String {
    let mut content = remove_managed_block(existing, ROOT_BLOCK_BEGIN, ROOT_BLOCK_END);
    content = ensure_codex_required_features(&content);
    let prefix = content.trim();
    if prefix.is_empty() {
        format!(
            "[features]\nhooks = true\nunified_exec = true\n\n{}\n",
            block.trim_end()
        )
    } else {
        format!("{}\n\n{}\n", prefix, block.trim_end())
    }
}

