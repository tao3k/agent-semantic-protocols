fn project_root_for_codex_config_path(project_config_path: &Path) -> Result<PathBuf, String> {
    let codex_dir = project_config_path
        .parent()
        .ok_or_else(|| "Codex config path has no parent directory".to_string())?;
    if codex_dir.file_name().and_then(|value| value.to_str()) != Some(".codex") {
        return Err(format!(
            "Codex config path {} is not under a .codex directory",
            project_config_path.display()
        ));
    }
    codex_dir
        .parent()
        .map(Path::to_path_buf)
        .ok_or_else(|| "Codex config path has no project root".to_string())
}

fn shell_single_quoted(value: &str) -> String {
    format!("'{}'", value.replace('\'', "'\"'\"'"))
}

fn is_managed_claude_group(group: &Value) -> bool {
    group
        .get("hooks")
        .and_then(Value::as_array)
        .is_some_and(|hooks| hooks.iter().any(is_managed_claude_hook))
}

fn is_managed_claude_hook(hook: &Value) -> bool {
    hook.get("command")
        .and_then(Value::as_str)
        .is_some_and(|command| {
            command.contains(CLAUDE_MANAGED_COMMAND_MARKER)
                || (command.contains("asp hook") && command.contains("--client claude"))
        })
}

fn canonical_json(value: Value) -> Value {
    match value {
        Value::Object(map) => {
            let mut keys = map.keys().cloned().collect::<Vec<_>>();
            keys.sort();
            let mut sorted = serde_json::Map::new();
            for key in keys {
                if let Some(value) = map.get(&key) {
                    sorted.insert(key, canonical_json(value.clone()));
                }
            }
            Value::Object(sorted)
        }
        Value::Array(items) => Value::Array(items.into_iter().map(canonical_json).collect()),
        other => other,
    }
}

fn ensure_codex_required_features(existing: &str) -> String {
    let mut table = String::new();
    let mut lines = existing
        .lines()
        .filter_map(|line| {
            let trimmed = line.trim();
            let skip = is_top_level_codex_feature(&table, trimmed);
            if let Some(header) = toml_table_header(trimmed) {
                table = header;
            }
            (!skip).then(|| line.to_string())
        })
        .collect::<Vec<_>>();

    let Some(features_start) = lines
        .iter()
        .position(|line| toml_table_header(line.trim()).as_deref() == Some("features"))
    else {
        let body = lines.join("\n").trim().to_string();
        return if body.is_empty() {
            "[features]\nhooks = true\nunified_exec = true".to_string()
        } else {
            format!("[features]\nhooks = true\nunified_exec = true\n\n{body}")
        };
    };

    let mut features_end = lines
        .iter()
        .enumerate()
        .skip(features_start + 1)
        .find_map(|(index, line)| toml_table_header(line.trim()).map(|_| index))
        .unwrap_or(lines.len());

    for key in ["hooks", "unified_exec"] {
        ensure_codex_feature_key(&mut lines, features_start, &mut features_end, key);
    }
    lines.join("\n")
}

fn ensure_codex_feature_key(
    lines: &mut Vec<String>,
    features_start: usize,
    features_end: &mut usize,
    key: &str,
) {
    if let Some(index) = lines[features_start + 1..*features_end]
        .iter()
        .position(|line| is_codex_feature_key(line.trim(), key))
        .map(|relative_index| features_start + 1 + relative_index)
    {
        lines[index] = format!("{key} = true");
    } else {
        lines.insert(*features_end, format!("{key} = true"));
        *features_end += 1;
    }
}

fn is_top_level_codex_feature(table: &str, trimmed: &str) -> bool {
    table.is_empty()
        && ["hooks", "unified_exec"]
            .iter()
            .any(|key| is_codex_feature_key(trimmed, key))
}

fn is_codex_feature_key(trimmed: &str, key: &str) -> bool {
    if trimmed.is_empty() || trimmed.starts_with('#') {
        return false;
    }
    trimmed
        .split_once('=')
        .is_some_and(|(candidate, _)| candidate.trim() == key)
}

fn toml_table_header(trimmed: &str) -> Option<String> {
    if !trimmed.starts_with('[') || !trimmed.ends_with(']') {
        return None;
    }
    let header = trimmed.trim_matches(['[', ']']).trim();
    (!header.is_empty()).then(|| header.to_string())
}

fn remove_managed_block(existing: &str, begin: &str, end: &str) -> String {
    let mut content = existing.to_string();
    while let Some(start) = content.find(begin) {
        let Some(end_marker_start) = content[start..].find(end) else {
            break;
        };
        let end_index = start + end_marker_start + end.len();
        content.replace_range(start..end_index, "");
    }
    content.trim().to_string()
}
