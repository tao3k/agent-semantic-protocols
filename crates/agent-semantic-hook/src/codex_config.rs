fn codex_hook_event_block(event: &CodexHookEvent, project_root: &Path) -> String {
    let matcher_line = event
        .matcher
        .map(|value| format!("matcher = {}\n\n", toml_basic_string(value)))
        .unwrap_or_else(|| "\n".to_string());
    let command = codex_hook_command(event.hook_event, project_root);
    format!(
        "[[hooks.{event_name}]]\n{matcher_line}[[hooks.{event_name}.hooks]]\ntype = \"command\"\ntimeout = 5\nstatusMessage = \"{status}\"\ncommand = '''\n{command}'''",
        event_name = event.event,
        status = event.status,
    )
}

fn codex_hook_command(hook_event: &str, project_root: &Path) -> String {
    let activation_path = project_activation_path(project_root)
        .expect("State Core activation path should resolve for Codex hook config");
    let project_root = shell_single_quoted(&project_root.display().to_string());
    let activation_path = shell_single_quoted(&activation_path.display().to_string());
    format!(
        "repo_root={project_root}\ncd \"$repo_root\"\nactivation={activation_path}\nexec direnv exec \"$repo_root\" \"$repo_root/.bin/asp\" hook {hook_event} --client codex --activation \"$activation\"\n"
    )
}

fn codex_hook_state_block(
    config_source_path: &Path,
    event: &CodexHookEvent,
    project_root: &Path,
) -> String {
    let key = format!("{}:{}:0:0", config_source_path.display(), event.state_label);
    let hash = codex_hook_trusted_hash(event, project_root);
    format!(
        "[hooks.state.{}]\ntrusted_hash = {}",
        toml_basic_string(&key),
        toml_basic_string(&hash)
    )
}

fn codex_home_path() -> Result<PathBuf, String> {
    if let Some(path) = env::var_os("CODEX_HOME").filter(|value| !value.is_empty()) {
        return Ok(PathBuf::from(path));
    }
    env::var_os("HOME")
        .filter(|value| !value.is_empty())
        .map(|home| PathBuf::from(home).join(".codex"))
        .ok_or_else(|| {
            "missing CODEX_HOME and HOME; cannot write Codex hook trust state".to_string()
        })
}

fn codex_trust_state_block(config_source_path: &Path, project_root: &Path) -> String {
    let state = codex_hook_events()
        .iter()
        .map(|event| codex_hook_state_block(config_source_path, event, project_root))
        .collect::<Vec<_>>()
        .join("\n\n");
    format!(
        "{}\n{state}\n{TRUST_BLOCK_END}",
        codex_trust_block_begin(config_source_path)
    )
}

fn codex_hook_trusted_hash(event: &CodexHookEvent, project_root: &Path) -> String {
    let mut identity = serde_json::Map::new();
    identity.insert(
        "event_name".to_string(),
        Value::String(event.state_label.to_string()),
    );
    if let Some(matcher) = event.matcher {
        identity.insert("matcher".to_string(), Value::String(matcher.to_string()));
    }
    identity.insert(
        "hooks".to_string(),
        Value::Array(vec![json!({
            "type": "command",
            "command": codex_hook_command(event.hook_event, project_root),
            "timeout": 5,
            "async": false,
            "statusMessage": event.status,
        })]),
    );
    version_for_codex_hook_identity(Value::Object(identity))
}

fn version_for_codex_hook_identity(value: Value) -> String {
    let canonical = canonical_json(value);
    let serialized = serde_json::to_vec(&canonical).unwrap_or_default();
    let hash = Sha256::digest(serialized);
    let hex = hash
        .iter()
        .map(|byte| format!("{byte:02x}"))
        .collect::<String>();
    format!("sha256:{hex}")
}

