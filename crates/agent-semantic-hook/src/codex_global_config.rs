fn codex_global_hook_event_block(
    event: &CodexGlobalHookEvent,
    asp_binary: Option<&Path>,
) -> String {
    let matcher_line = event
        .matcher
        .map(|value| format!("matcher = {}\n\n", toml_basic_string(value)))
        .unwrap_or_else(|| "\n".to_string());
    let command = codex_global_hook_command(event.hook_event, asp_binary);
    format!(
        "[[hooks.{event_name}]]\n{matcher_line}[[hooks.{event_name}.hooks]]\ntype = \"command\"\ntimeout = 5\nstatusMessage = \"{status}\"\ncommand = '''\n{command}'''",
        event_name = event.state_label,
        status = event.status,
    )
}

fn codex_global_hook_command(hook_event: &str, asp_binary: Option<&Path>) -> String {
    let asp_binary = asp_binary
        .map(|path| shell_single_quoted(&path.display().to_string()))
        .unwrap_or_else(|| "asp".to_string());
    format!(
        "repo_root=\"${{CODEX_WORKSPACE_ROOT:-${{PWD:-.}}}}\"\ncd \"$repo_root\"\nexec direnv exec \"$repo_root\" {asp_binary} hook {hook_event} --client codex\n"
    )
}

fn codex_global_hook_trust_state_block(
    config_source_path: &Path,
    asp_binary: Option<&Path>,
) -> String {
    let state = codex_global_hook_events()
        .iter()
        .map(|event| codex_global_hook_state_block(config_source_path, event, asp_binary))
        .collect::<Vec<_>>()
        .join("\n\n");
    format!(
        "{}{}\n{state}\n{TRUST_BLOCK_END}",
        TRUST_BLOCK_BEGIN_PREFIX,
        config_source_path.display()
    )
}

fn codex_global_hook_state_block(
    config_source_path: &Path,
    event: &CodexGlobalHookEvent,
    asp_binary: Option<&Path>,
) -> String {
    let key = format!("{}:{}:0:0", config_source_path.display(), event.state_label);
    let hash = codex_global_hook_trusted_hash(event, asp_binary);
    format!(
        "[hooks.state.{}]\ntrusted_hash = {}",
        toml_basic_string(&key),
        toml_basic_string(&hash)
    )
}

fn codex_global_hook_trusted_hash(
    event: &CodexGlobalHookEvent,
    asp_binary: Option<&Path>,
) -> String {
    let mut identity = serde_json::Map::new();
    identity.insert(
        "event_name".to_string(),
        Value::String(event.state_label.to_string()),
    );
    if let Some(matcher) = event.matcher {
        identity.insert("matcher".to_string(), Value::String(matcher.to_string()));
    }
    identity.insert(
        "hooks".to_string(),
        Value::Array(vec![json!({
            "type": "command",
            "command": codex_global_hook_command(event.hook_event, asp_binary),
            "timeout": 5,
            "async": false,
            "statusMessage": event.status,
        })]),
    );
    version_for_codex_hook_identity(Value::Object(identity))
}

fn remove_codex_global_hook_trust_block(existing: &str, config_source_path: &Path) -> String {
    let begin = format!("{TRUST_BLOCK_BEGIN_PREFIX}{}", config_source_path.display());
    let lines = existing.lines().collect::<Vec<_>>();
    let mut kept = Vec::new();
    let mut index = 0;
    while index < lines.len() {
        if lines[index].trim() == begin {
            index += 1;
            while index < lines.len() && lines[index].trim() != TRUST_BLOCK_END {
                index += 1;
            }
            if index < lines.len() {
                index += 1;
            }
            continue;
        }
        kept.push(lines[index]);
        index += 1;
    }
    kept.join("\n")
}

fn version_for_codex_hook_identity(value: Value) -> String {
    let canonical = canonical_json(value);
    let serialized = serde_json::to_vec(&canonical).unwrap_or_default();
    let hash = Sha256::digest(serialized);
    let hex = hash
        .iter()
        .map(|byte| format!("{byte:02x}"))
        .collect::<String>();
    format!("sha256:{hex}")
}

fn canonical_json(value: Value) -> Value {
    match value {
        Value::Object(map) => {
            let mut keys = map.keys().cloned().collect::<Vec<_>>();
            keys.sort();
            let mut sorted = serde_json::Map::new();
            for key in keys {
                if let Some(value) = map.get(&key) {
                    sorted.insert(key, canonical_json(value.clone()));
                }
            }
            Value::Object(sorted)
        }
        Value::Array(items) => Value::Array(items.into_iter().map(canonical_json).collect()),
        other => other,
    }
}

fn toml_basic_string(value: &str) -> String {
    format!("{value:?}")
}

fn shell_single_quoted(value: &str) -> String {
    format!("'{}'", value.replace('\'', "'\"'\"'"))
}
