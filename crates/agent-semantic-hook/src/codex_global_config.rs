use crate::codex_config::{ROOT_BLOCK_BEGIN, ROOT_BLOCK_END, codex_asp_explorer_role_block};

const TOOL_SURFACE_MATCHER: &str = r"Read|read|readFile|readDirectory|read_file|read_directory|FsReadFile|FsReadDirectory|fs\.read|fs\.readFile|fs\.readDirectory|fs/read|fs/readFile|fs/readDirectory|fs\\read|fs\\readFile|fs\\readDirectory|functions\.read|functions\.read_file|functions\.readFile|mcp__.*__read|mcp__.*__read_file|mcp__.*__readFile|functions\.exec_command|exec_command|command_execution|multi_tool_use\.parallel|Bash|Shell";
const TRUST_BLOCK_BEGIN_PREFIX: &str = "# BEGIN agent-semantic-protocol trusted hook state: ";
const TRUST_BLOCK_END: &str = "# END agent-semantic-protocol trusted hook state";

#[derive(Debug, Clone, Copy)]
struct CodexGlobalHookEvent {
    state_label: &'static str,
    matcher: Option<&'static str>,
    status: &'static str,
    hook_event: &'static str,
}

/// Render a user-level Codex hook block that dispatches through `asp hook`.
///
/// User-level hooks are project-neutral. The hook runtime resolves the current
/// workspace from Codex's working directory instead of hard-coding the project
/// that installed the hook.
pub fn codex_global_hook_block_with_binary(asp_binary: Option<&Path>) -> String {
    let body = codex_global_hook_events()
        .iter()
        .map(|event| codex_global_hook_event_block(event, asp_binary))
        .collect::<Vec<_>>()
        .join("\n\n");
    format!(
        "{ROOT_BLOCK_BEGIN}\n# Generated by `asp install plugin --codex`.\n# ASP Explorer role registration for Codex spawn_agent.\n\n{}\n\n# User-level dispatcher for activated semantic hook providers.\n\n{body}\n{ROOT_BLOCK_END}",
        codex_asp_explorer_role_block()
    )
}

/// Merge trust state for the user-level Codex hook block into Codex config.
pub fn merge_codex_global_hook_trust_config(
    existing: &str,
    config_source_path: &Path,
    asp_binary: Option<&Path>,
) -> String {
    let content = remove_codex_global_hook_trust_block(existing, config_source_path);
    let trust_block = codex_global_hook_trust_state_block(config_source_path, asp_binary);
    let prefix = content.trim_end();
    if prefix.is_empty() {
        format!("{trust_block}\n")
    } else {
        format!("{prefix}\n\n{trust_block}\n")
    }
}

