use serde_json::{Value, json};
use sha2::{Digest, Sha256};
use std::env;
use std::fs;
use std::path::{Path, PathBuf};

pub(crate) const ROOT_BLOCK_BEGIN: &str = "# BEGIN semantic-agent-hook agent hooks";
pub(crate) const ROOT_BLOCK_END: &str = "# END semantic-agent-hook agent hooks";

const TRUST_BLOCK_END: &str = "# END semantic-agent-hook trusted hook state";
const LEGACY_BLOCKS: [(&str, &str); 3] = [
    (
        "# BEGIN ts-harness agent hooks",
        "# END ts-harness agent hooks",
    ),
    (
        "# BEGIN py-harness agent hooks",
        "# END py-harness agent hooks",
    ),
    (
        "# BEGIN rs-harness agent hooks",
        "# END rs-harness agent hooks",
    ),
];
const CODEX_TOOL_MATCHER: &str = ".*";

#[derive(Debug)]
pub(crate) struct CodexUserTrustStatus {
    pub trust_config_path: PathBuf,
    pub trusted: bool,
    pub missing_events: Vec<String>,
}

#[derive(Debug, Clone, Copy)]
struct CodexHookEvent {
    event: &'static str,
    state_label: &'static str,
    matcher: Option<&'static str>,
    status: &'static str,
    hook_event: &'static str,
}

pub(crate) fn codex_hook_block() -> String {
    let events = codex_hook_events();
    let body = events
        .iter()
        .map(codex_hook_event_block)
        .collect::<Vec<_>>()
        .join("\n\n");
    format!(
        "{ROOT_BLOCK_BEGIN}\n# Generated by `semantic-agent-hook install --client codex`.\n# Root dispatcher for language-owned semantic hook profiles.\n\n{body}\n{ROOT_BLOCK_END}"
    )
}

pub(crate) fn install_codex_user_trust_state(
    project_config_path: &Path,
) -> Result<PathBuf, String> {
    let project_config_path = fs::canonicalize(project_config_path).map_err(|error| {
        format!(
            "failed to resolve project Codex config {}: {error}",
            project_config_path.display()
        )
    })?;
    let codex_home = codex_home_path()?;
    fs::create_dir_all(&codex_home)
        .map_err(|error| format!("failed to create {}: {error}", codex_home.display()))?;
    let user_config_path = codex_home.join("config.toml");
    let existing = fs::read_to_string(&user_config_path).unwrap_or_default();
    if user_config_path.is_file() {
        validate_codex_config_toml(&existing).map_err(|error| {
            format!(
                "refusing to write invalid Codex user config {}: {error}",
                user_config_path.display()
            )
        })?;
    }
    let trust_block = codex_trust_state_block(&project_config_path);
    let merged = merge_codex_trust_config(&existing, &project_config_path, &trust_block);
    validate_codex_config_toml(&merged).map_err(|error| {
        format!(
            "refusing to write invalid Codex user trust config {}: {error}",
            user_config_path.display()
        )
    })?;
    fs::write(&user_config_path, merged.as_bytes())
        .map_err(|error| format!("failed to write {}: {error}", user_config_path.display()))?;
    Ok(user_config_path)
}

pub(crate) fn codex_user_trust_state_status(
    project_config_path: &Path,
) -> Result<CodexUserTrustStatus, String> {
    let project_config_path = fs::canonicalize(project_config_path).map_err(|error| {
        format!(
            "failed to resolve project Codex config {}: {error}",
            project_config_path.display()
        )
    })?;
    let trust_config_path = codex_home_path()?.join("config.toml");
    let content = fs::read_to_string(&trust_config_path).unwrap_or_default();
    let parsed =
        toml::from_str::<toml::Value>(&content)
            .unwrap_or(toml::Value::Table(
                toml::map::Map::<String, toml::Value>::new(),
            ));
    let state = parsed
        .get("hooks")
        .and_then(toml::Value::as_table)
        .and_then(|hooks| hooks.get("state"))
        .and_then(toml::Value::as_table);
    let missing_events = codex_hook_events()
        .iter()
        .filter_map(|event| {
            let key = format!(
                "{}:{}:0:0",
                project_config_path.display(),
                event.state_label
            );
            let expected_hash = codex_hook_trusted_hash(event);
            let trusted = state
                .and_then(|state| state.get(&key))
                .and_then(toml::Value::as_table)
                .and_then(|entry| entry.get("trusted_hash"))
                .and_then(toml::Value::as_str)
                .is_some_and(|actual_hash| actual_hash == expected_hash);
            (!trusted).then(|| event.hook_event.to_string())
        })
        .collect::<Vec<_>>();
    Ok(CodexUserTrustStatus {
        trust_config_path,
        trusted: missing_events.is_empty(),
        missing_events,
    })
}

pub(crate) fn validate_codex_config_toml(content: &str) -> Result<(), String> {
    toml::from_str::<toml::Value>(content)
        .map(|_| ())
        .map_err(|error| error.to_string())
}

pub(crate) fn merge_codex_config(existing: &str, block: &str) -> String {
    let mut content = existing.to_string();
    for (begin, end) in LEGACY_BLOCKS {
        content = remove_managed_block(&content, begin, end);
    }
    content = remove_managed_block(&content, ROOT_BLOCK_BEGIN, ROOT_BLOCK_END);
    content = ensure_codex_required_features(&content);
    let prefix = content.trim();
    if prefix.is_empty() {
        format!(
            "[features]\nhooks = true\nunified_exec = true\n\n{}\n",
            block.trim_end()
        )
    } else {
        format!("{}\n\n{}\n", prefix, block.trim_end())
    }
}

